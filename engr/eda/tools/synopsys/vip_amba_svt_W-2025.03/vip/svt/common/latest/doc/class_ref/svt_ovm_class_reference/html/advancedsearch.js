
function WriteSymbolEntry( teststring, symbol_string,offset)
{
  var pos,p1,p2;
  var name;
  var count;
  var plural;
  var htmllink = "_";
  var name;
  var name1;
  var name2;
  var name3;
  var exactMatch = -1;
  var symbol_line = "" + symbol_string;

  pos   = symbol_line.search(" ");     // find where to split the symbol string
  count = symbol_line.substr( 0, pos );  // 1st part is the count
  name  = symbol_line.substr( pos+1 );   // 2nd part is the name

    if (name.substr(0, 1)=="a") {
      htmllink = offset+"indexall.html#name_" + name;
    }
    else {
      htmllink = offset+"indexall_" + name.substring(0,1) + ".html#name_" + name;
    }

  if (count > 1) { plural = "s"; } else { plural = ""; }

  document.write( "<tr>\n");

  /*
  ** Is this symbol line an exact name match?
  */
  if (teststring == name || teststring == "") {
    document.write( "  <td class=\"SearchResults\"><a class=\"SearchResults\" href=\"" + htmllink + "\"><b>" + name + "</b></a></td>\n");
    document.write( "  <td class=\"SearchResults\">" + count + " element" + plural + " matched ...</td>");
  }
  /*
  ** Deal with an inexact name match (the teststring gets bolded)
  */
  else {
    p1 = name.search(teststring);
    if (p1 > 0) {
      name1 = name.substr(0, p1);
    }
    else {
      name1 = "";
    }
    p2 = p1 + teststring.length;
    name2 = name.substring( p1, p2 );

    if (p2 < name.length) {;
      name3 = name.substring(p2);
    }
    else {
      name3 = "";
    }

    document.write( "  <td class=\"SearchResults\"><a class=\"SearchResults\" href=\"" + htmllink + "\">" );
    document.write(       name1 + "<b>" + name2 + "</b>" + name3 + "</a></td>");
    document.write( "  <td class=\"SearchResults\">" + count + " element" + plural + " matched ...</td>");
  }
  document.write( "</tr>");
}

function TestExact( match, symbol_entry )
{
  var pos;
  var name;
  var count;
  var symbol_line = "" + symbol_entry;

  pos = symbol_line.search( " " );
  count = symbol_line.substring( 0, pos );
  name  = symbol_line.substring( pos+1 );

  if (name == match) {
    return 1;
  }
  return 0;
}

function TestPartial( match, symbol_entry )
{
  var pos;
  var name;
  var count;
  var symbol_line = "" + symbol_entry;

  pos = symbol_line.search( " " );
  count = symbol_line.substring( 0, pos );
  name  = symbol_line.substring( pos+1 );

  if (name == match) {
    return 2;
  }
  if (name.search(match) >= 0) {  // returns -1 if not found
    return 1;
  }
  return 0;
}


